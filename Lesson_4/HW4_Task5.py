s = str(input())
print(f'Третий символ сторки: "{s[2]}"')
print(f'Предпоследний символ сторки: "{s[-2]}"')
print(f'Первые 5 символов сторки: "{s[:5]}"')
print(f'Вся строка, кроме последних двух символов: "{s[:-2]}"')
print(f'Все символы с четными индексами: "{s[0::2]}"')
print(f'Все символы с нечетными индексами: "{s[1::2]}"')
print(f'Все символы в обратном порядке: "{s[::-1]}"')
print(f'Все символы через один в обратном порядке, начиная с последнего: "{s[:0:-2]}"')
print(f'Длина строки: "{len(s)}"')